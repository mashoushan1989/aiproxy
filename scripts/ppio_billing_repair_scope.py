#!/usr/bin/env python3
from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Select the minimal billing-repair scope from a day+model diff CSV.",
    )
    parser.add_argument(
        "--diff-csv",
        required=True,
        help="Path to ppio_vs_local_by_day_model.csv",
    )
    parser.add_argument("--start", required=True, help="Inclusive start date YYYY-MM-DD")
    parser.add_argument("--end", required=True, help="Inclusive end date YYYY-MM-DD")
    parser.add_argument(
        "--amount-threshold",
        type=float,
        default=500.0,
        help="Absolute delta threshold in RMB",
    )
    parser.add_argument(
        "--ratio-threshold",
        type=float,
        default=0.05,
        help="Absolute ratio threshold around 1.0, e.g. 0.05 means +/-5%%",
    )
    parser.add_argument(
        "--output-dir",
        default="tmp/ppio_repair_scope",
        help="Directory for generated scope files",
    )
    parser.add_argument(
        "--label",
        default="phase_a",
        help="Label prefix used for output filenames",
    )
    return parser.parse_args()


def write_scope_sql(scope: pd.DataFrame, path: Path) -> None:
    lines = [
        "-- Generated by scripts/ppio_billing_repair_scope.py",
        "DROP TABLE IF EXISTS tmp_ppio_repair_scope;",
        "CREATE TEMP TABLE tmp_ppio_repair_scope (",
        "  day date NOT NULL,",
        "  model text NOT NULL,",
        "  book_used_amount numeric NOT NULL,",
        "  local_used_amount numeric NOT NULL,",
        "  delta_used_amount numeric NOT NULL,",
        "  ratio_used_amount numeric NOT NULL,",
        "  PRIMARY KEY (day, model)",
        ");",
        "",
    ]
    if scope.empty:
        lines.append("-- Scope is empty.")
        path.write_text("\n".join(lines) + "\n", encoding="utf-8")
        return

    lines.extend(
        [
            "INSERT INTO tmp_ppio_repair_scope (",
            "  day, model, book_used_amount, local_used_amount, delta_used_amount, ratio_used_amount",
            ") VALUES",
        ]
    )
    values = []
    for row in scope.itertuples(index=False):
        model = str(row.model).replace("'", "''")
        values.append(
            "  ('{day}', '{model}', {book:.8f}, {local:.8f}, {delta:.8f}, {ratio:.8f})".format(
                day=row.day,
                model=model,
                book=float(row.book_used_amount),
                local=float(row.local_used_amount),
                delta=float(row.delta_used_amount),
                ratio=float(row.ratio_used_amount),
            )
        )
    lines.append(",\n".join(values) + ";")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(args.diff_csv)
    df["day"] = pd.to_datetime(df["day"], errors="coerce").dt.date

    start = pd.to_datetime(args.start).date()
    end = pd.to_datetime(args.end).date()
    sub = df[(df["day"] >= start) & (df["day"] <= end)].copy()

    sub["abs_delta_used_amount"] = pd.to_numeric(
        sub["delta_used_amount"], errors="coerce"
    ).abs()
    sub["ratio_deviation"] = (
        pd.to_numeric(sub["ratio_used_amount"], errors="coerce").fillna(0) - 1.0
    ).abs()

    scope = sub[
        (sub["abs_delta_used_amount"] >= args.amount_threshold)
        | (sub["ratio_deviation"] >= args.ratio_threshold)
    ].copy()
    scope = scope.sort_values(
        ["day", "abs_delta_used_amount"], ascending=[True, False]
    )[
        [
            "day",
            "model",
            "book_used_amount",
            "local_used_amount",
            "delta_used_amount",
            "ratio_used_amount",
            "abs_delta_used_amount",
            "ratio_deviation",
        ]
    ]

    csv_path = out_dir / f"{args.label}_repair_scope.csv"
    sql_path = out_dir / f"{args.label}_repair_scope.sql"
    scope.to_csv(csv_path, index=False)
    write_scope_sql(scope, sql_path)

    print("Selected rows:", len(scope))
    print("Total book:", round(float(scope["book_used_amount"].sum()), 4))
    print("Total local:", round(float(scope["local_used_amount"].sum()), 4))
    print("Total delta:", round(float(scope["delta_used_amount"].sum()), 4))
    print("Wrote:", csv_path)
    print("Wrote:", sql_path)


if __name__ == "__main__":
    main()
